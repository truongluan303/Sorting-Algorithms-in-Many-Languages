#define SORT_H


/*************************************************************
 * @brief sorting using bubble sort
 * 
 * @param arr   the array to be sorted
 * @param size  the size of the given array
 */
void bubble_sort(int*, unsigned int);



/*************************************************************
 * @brief sorting using selection sort
 * 
 * @param arr   the array to be sorted
 * @param size  the size of the given array
 */
void selection_sort(int*, unsigned int);



/*************************************************************
 * @brief sorting using insertion sort
 * 
 * @param arr   the array to be sorted 
 * @param size  the size of the given array
 */
void insertion_sort(int*, unsigned int);



/*************************************************************
 * @brief sorting using merge sort
 * 
 * @param arr   the array to be sorted
 * @param size  the size of the given array
 */
void merge_sort(int*, unsigned int);



/*************************************************************
 * @brief sorting using quick sort
 * 
 * @param arr   the array to be sorted
 * @param size  the size of the given array
 */
void quick_sort(int*, unsigned int);